    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        contaapagar_ c = this.MontarContaaPagar();
         if  (c != null) {
             ContasaPagarControle cp = new ContasaPagarControle ();
             cp.insert(c);
        }else {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos: ");
        
        }           
                        
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        contaapagar_ c = this.MontarContaaPagar();
        if  (c != null && !txtfCodcontasapagar.getText().trim().isEmpty()){
             c.setCodcontaapagar_(Integer.parseInt(txtfCodcontasapagar.getText()));
             ContasaPagarControle cp = new ContasaPagarControle ();
             cp.update(c);
        }else {
              JOptionPane.showMessageDialog(null, "Preencha todos os campos.");     
        }  
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        ContasaPagarControle cp = new ContasaPagarControle ();
        if(!txtfCodcontasapagar.getText().trim().isEmpty()){
            int Cod = Integer.parseInt(txtfCodcontasapagar.getText());
            cp.delete(Cod);
         }else{
            JOptionPane.showMessageDialog(null,"Informe novamente...");    
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

