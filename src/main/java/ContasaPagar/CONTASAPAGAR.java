    public contaapagar_ MontarContaaPagar() {
       
           if (txtfDescricaodaConta.getText().trim().isEmpty() || txtfValor.getText().trim().isEmpty() || txtfDataVencimento.getText().trim().isEmpty() 
               || txtfDataPagamento.getText().trim().isEmpty() || txtfFornecedor.getText().trim().isEmpty() || txtfCategoria.getText().trim().isEmpty() 
               || txtfStatusPagamento.getText().trim().isEmpty() || txtfMetodoPagamento.getText().trim().isEmpty() || txtfObservacoes.getText().trim().isEmpty())
               
            { JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
                return null; 
            }
           
        try{   
           contaapagar_ cn = new contaapagar_ ();
           cn.setDescricao_da_Conta(txtfDescricaodaConta.getText().trim());
           cn.setValor(txtfValor.getText().trim());
           cn.setData_Vencimento(txtfDataVencimento.getText().trim());
           cn.setData_Pagamento(txtfDataPagamento.getText().trim());
           cn.setFornecedor(txtfFornecedor.getText().trim());
           cn.setCategoria(txtfCategoria.getText().trim());
           cn.setStatus_Pagamento(txtfStatusPagamento.getText().trim());
           cn.setMetodo_Pagamento(txtfMetodoPagamento.getText().trim());
           cn.setObservacoes(txtfObservacoes.getText().trim());
        
    return cn;    
     
         }catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(null, " inválido. Digite.", "Erro", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }
        
