    public eventos_ MontarEventos () {
        
           if (txtfNomedoEvento.getText().trim().isEmpty() || txtfDatadoEvento.getText().trim().isEmpty() || txtfHorarioInicio.getText().trim().isEmpty() 
               || txtfHorarioTermino.getText().trim().isEmpty() || txtfLocaldoEvento.getText().trim().isEmpty() || txtfDescricao.getText().trim().isEmpty() 
               || txtfPublicoAlvo.getText().trim().isEmpty() || txtfCapacidadeMaxima.getText().trim().isEmpty() || txtfOrganizador.getText().trim().isEmpty())
               
            { JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
                return null; 
            }
        
        
        try{      
           eventos_ e = new eventos_ ();
           e.setNome_do_Evento(txtfNomedoEvento.getText().trim());
           e.setData_do_Evento(txtfDatadoEvento.getText().trim());
           e.setHorario_Inicio(txtfHorarioInicio.getText().trim());
           e.setHorario_Termino(txtfHorarioTermino.getText().trim());
           e.setLocal_do_Evento(txtfLocaldoEvento.getText().trim());
           e.setDescricao(txtfDescricao.getText().trim());
           e.setPublico_Alvo(txtfPublicoAlvo.getText().trim());
           e.setCapacidade_Maxima(txtfCapacidadeMaxima.getText().trim());
           e.setOrganizador(txtfOrganizador.getText().trim());
        
        return e;  
        
        }catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(null, " inválido. Digite.", "Erro", JOptionPane.ERROR_MESSAGE);
            return null;
        
     }
  }   


