    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        eventos_ e = this.MontarEventos();
         if  (e != null) {
        EventosControle ev = new EventosControle ();
        ev.insert(e);
        }else {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos: ");
        
        }
        
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
         eventos_ e = this.MontarEventos();
         if  (e != null && !txtfCodeventos.getText().trim().isEmpty()){
             e.setCodeventos_(Integer.parseInt(txtfCodeventos.getText()));
             EventosControle ev = new EventosControle ();
             ev.update(e);
         }else {
              JOptionPane.showMessageDialog(null, "Preencha todos os campos.");    
         }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
         EventosControle ev = new EventosControle ();
         if(!txtfCodeventos.getText().trim().isEmpty()){
                int Cod = Integer.parseInt(txtfCodeventos.getText());
                ev.delete(Cod);
             }else{
            JOptionPane.showMessageDialog(null,"Informe novamente...");
       }
                
            
                                       
    }//GEN-LAST:event_btnFecharActionPerformed

