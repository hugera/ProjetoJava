    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        funcionario_ f = this.MontarFuncionario();
         if  (f != null) {
        FuncionarioControle fu = new FuncionarioControle();
        fu.insert(f);
        }else {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos: ");
        
        }
        
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        funcionario_ f = this.MontarFuncionario();
           if  (f != null && !txtfCodfuncionario.getText().trim().isEmpty()){
                f.setCodfuncionario_(Integer.parseInt(txtfCodfuncionario.getText()));
                FuncionarioDao fu = new FuncionarioDao();
                fu.update(f);
           }else {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos: ");
                
           }       
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         FuncionarioControle fu = new FuncionarioControle();
         if(!txtfCodfuncionario.getText().trim().isEmpty()){
                int Cod = Integer.parseInt(txtfCodfuncionario.getText());
                fu.delete(Cod);
             }else{
            JOptionPane.showMessageDialog(null,"Informe novamente...");
       }
    }//GEN-LAST:event_btnExcluirActionPerformed

