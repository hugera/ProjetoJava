    public funcionario_ MontarFuncionario () {
           
           if (txtfNome.getText().trim().isEmpty() || txtfCPF.getText().trim().isEmpty() || txtfEndereco.getText().trim().isEmpty() || txtfTelefone.getText().trim().isEmpty() 
               || txtfEmail.getText().trim().isEmpty() || txtfDataAdmissao.getText().trim().isEmpty() || txtfCargo.getText().trim().isEmpty() 
               || txtfSalario.getText().trim().isEmpty() || txtfDepartamento.getText().trim().isEmpty())
               
             { JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
                return null; 
            }
        
        try{    
            funcionario_ f = new funcionario_ ();
            f.setNome(txtfNome.getText().trim());
            f.setCPF(txtfCPF.getText().trim());
            f.setEndereco(txtfEndereco.getText().trim());
            f.setTelefone(txtfTelefone.getText().trim());
            f.setEmail(txtfEmail.getText().trim());
            f.setData_Admissao(txtfDataAdmissao.getText().trim());
            f.setCargo(txtfCargo.getText().trim());
            f.setSalario(txtfSalario.getText().trim());
            f.setDepartamento(txtfDepartamento.getText().trim());
        
        return f;   
        
        }catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(null, " inválido. Digite.", "Erro", JOptionPane.ERROR_MESSAGE);
            return null;
        
        }
    
    }
        


